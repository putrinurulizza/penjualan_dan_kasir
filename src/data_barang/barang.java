    private void AmbilKategori(String kategori){
        String[] kt ={"","",""};
        cbkategori.removeAllItems();
        cbkategori.addItem(kategori);
        for(int i = 0;i<kt.length;i++){
            if(!kt[i].equals(kategori)){
                cbkategori.addItem(kt[i]); }
        }
    }
    
    private void AmbilSuplier(String suplier){
        String[] jb ={"","",""};
        cbsuplier.removeAllItems();
        cbsuplier.addItem(suplier);
        for(int i = 0;i<jb.length;i++){
            if(!jb[i].equals(suplier)){
                cbsuplier.addItem(jb[i]); }
        }
    }  
    
     public void dataKategori(){
        conn = Koneksi.getKoneksi();
        try {
            String query = "SELECT * FROM kategori";
            Statement st = Koneksi.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()) {                
                cbkategori.addItem(rs.getString("id_kategori")+ " - " +rs.getString("nama_ktgr"));
            }
            
            rs.last();
            int jumlahdata = rs.getRow();
            rs.first();
            
        } catch (SQLException e) {
        }
    }
    
    public void dataSuplier(){
        conn = Koneksi.getKoneksi();
        try {
            String query = "SELECT * FROM suplier";
            Statement st = Koneksi.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()) {                
                cbsuplier.addItem(rs.getString("id_suplier")+" - "+rs.getString("nama"));
            }
            
            rs.last();
            int jumlahdata = rs.getRow();
            rs.first();
            
        } catch (SQLException e) {
        }
    }

    

    

