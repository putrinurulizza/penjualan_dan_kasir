    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
       
             String tampilan ="yyyy-MM-dd" ; 
             SimpleDateFormat fm = new SimpleDateFormat(tampilan); 
       
        try{
             
            Connection conn = Koneksi.getKoneksi();
            PreparedStatement stmt = (PreparedStatement) conn.prepareStatement("INSERT INTO barang(kode_barang, nama_barang, satuan, id_ktgr, stok, harga_jual,  harga_beli, tanggal, idsuplier)VALUES(?,?,?,?,?,?,?,?,?)");
            
            stmt.setString(1,txid.getText());
            stmt.setString(2,txnama.getText());
            stmt.setString(3,txsatuan.getText());
            
            String[] id_ktgr = cbkategori.getSelectedItem().toString().split(" - ");
            int ktgr = Integer.parseInt(id_ktgr[0]);
            stmt.setInt(4, ktgr);
            
            stok = Integer.parseInt(txstok.getText());
            stmt.setInt(5, stok);
            
            hrgjl = Integer.parseInt(txhrgjl.getText());
            stmt.setInt(6, hrgjl);
            
             hrgbl = Integer.parseInt(txhrgbl.getText());
            stmt.setInt(7, hrgbl);
            
            tanggal = String.valueOf(fm.format(tgl.getDate()));
            stmt.setDate(8,Date.valueOf(tanggal));
            
            String[] id_spl = cbsuplier.getSelectedItem().toString().split(" - ");
            int spl = Integer.parseInt(id_spl[0]);
            stmt.setInt(9, spl);
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan!");
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        setVisible(false); 
        view_barang mb =new view_barang();
        mb.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnsimpanActionPerformed

